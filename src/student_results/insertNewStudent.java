    private void uploadBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadBtnActionPerformed
        // TODO add your handling code here:
        String admissionNum = admissionNo.getText();
        String subject1 = subject101.getText();
        String subject2 = subject102.getText();
        String subject3 = subject103.getText();
        String subject4 = subject104.getText();
        String subject5 = subject105.getText();
        
        int admissionNoParsed;
        int subject1Parsed;
        int subject2Parsed;
        int subject3Parsed;
        int subject4Parsed;
        int subject5Parsed;
        
        String databaseName = "StudentsResults";
        String url = "jdbc:mysql://localhost:3306/" + databaseName;
        String user = "root";
        String password = "database@MySql";
        String myDriver = "com.mysql.jdbc.Driver";
        try { 
                   admissionNoParsed = Integer.parseInt(admissionNum); 
                   subject1Parsed = Integer.parseInt(subject1); 
                   subject2Parsed = Integer.parseInt(subject2); 
                   subject3Parsed = Integer.parseInt(subject3); 
                   subject4Parsed = Integer.parseInt(subject4); 
                   subject5Parsed = Integer.parseInt(subject5); 
                   
                    //Insert data to SQL
                    java.sql.Connection conn = null;
                    ResultSet result = null;
                   //Initialize Sqldriver instance 
                  Class.forName(myDriver);
                  
                  // Creating the connection providing URL and username password
                  conn =  DriverManager.getConnection(url, user, password);
                  Statement st = conn.createStatement();
                  result = st.executeQuery("SELECT * FROM StudentResults WHERE admissionNoParsed = " +  admissionNoParsed);
                  if(result.next()) {
                        String insertData = "INSERT INTO StudentResults" 
                          + "( AdmissonNo, subject101, subject102, subject103, subject104,subject105) "
                          + "VALUES ( " +admissionNoParsed+ ',' +subject2Parsed+ ','  +subject3Parsed+ ',' +subject4Parsed+ ',' +subject5Parsed +")";
                        st.executeUpdate(insertData);
                        JOptionPane.showMessageDialog(null,  "Successfully inserted to database");
                        this.setVisible(false);
                         new insertNewStudent().setVisible(true);
                  }
                  else {
                        JOptionPane.showMessageDialog(null,  "Admission does not exist");                      
                  }
                  
                  
        } catch (Exception ex) {
        }
    }//GEN-LAST:event_uploadBtnActionPerformed

